a, b, c = map(int, input().split())

if a == b == c:
    print(10**4 + 10**3*a)
elif a == b:
    print(10**3+10**2*a)
elif b == c:
    print(10**3+10**2*b)
elif a == c:
    print(10**3+10**2*c)
else:
    print(10**2*max(a, b, c))