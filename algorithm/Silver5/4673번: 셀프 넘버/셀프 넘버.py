# 1부터 10,000까지의 숫자 중에서 생성자가 있는 숫자들을 찾기
# 1부터 10,000까지의 숫자를 순회하면서 각 숫자의 생성자를 계산하고, 생성자가 되는 숫자들을 모두 제외
# 생성자를 계산하는 함수 d(n)를 작성하고, 생성자를 계산하여 리스트에 추가하는 방식으로 셀프 넘버를 찾

def d(n):
    # n과 n의 각 자리수를 더하는 함수
    result = n
    while n > 0:
        result += n % 10
        n //= 10
    return result

# 1부터 10,000까지의 숫자 중에서 생성자가 있는 숫자들을 찾음
generated_numbers = set()
for i in range(1, 10001):
    generated_numbers.add(d(i))

# 1부터 10,000까지의 숫자 중에서 생성자가 없는 숫자를 출력
for i in range(1, 10001):
    if i not in generated_numbers:
        print(i)