# 3씩 빼나가다가, 5의 배수를 만나면 그만큼 빼기!
n = int(input())

cnt = 0
while n >= 0:
  if n % 5 == 0:
    cnt += n // 5
    print(cnt)
    break
  n -= 3
  cnt += 1
else:
  print(-1)